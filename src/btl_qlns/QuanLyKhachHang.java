    private void btXoaKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaKHActionPerformed

        int rowIndex = tbDSKH.getSelectedRow();

        if (rowIndex >= 0) {
            String maKH = tbDSKH.getValueAt(rowIndex, 0).toString();

            DefaultTableModel model = (DefaultTableModel) tbDSKH.getModel();
            model.removeRow(rowIndex);

            String deleteQuery = "DELETE FROM khachhang WHERE makh = '" + maKH + "'";

            int result = ketNoi.executeUpdate(deleteQuery);

            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Xóa khách hàng thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Xóa khách hàng thất bại.", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một khách hàng để xóa.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btXoaKHActionPerformed

