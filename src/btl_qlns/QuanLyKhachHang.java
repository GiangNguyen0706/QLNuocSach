    private void loadData(String query) {
        DefaultTableModel model = (DefaultTableModel) tbDSKH.getModel();
        model.setRowCount(0);
        tbDSKH.setAutoCreateRowSorter(true);
        ResultSet rs = ketNoi.executeQuery(query);

        try {
            while (rs.next()) {
                String maKH = rs.getString(1);
                String hoTen = rs.getString(2);
                String cmnd = rs.getString(3);
                String ngaySinh = rs.getString(4);
                String diaChi = rs.getString(5);
                String sdt = rs.getString(6);
                String mucDich = rs.getString(7);
                boolean thanhToan = rs.getBoolean(8);  // Assuming that the 8th column is the boolean column (thanhtoan)

                // Create an array with the data
                Object[] rowData = {maKH, hoTen, cmnd, ngaySinh, diaChi, sdt, mucDich, thanhToan};

                // Add the row to the model
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
        }
    }

