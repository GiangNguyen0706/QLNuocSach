    private void btSuaKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaKHActionPerformed
        if (txtMaKH.getText().equals("") || txtHoTenKH.getText().equals("") || txtCMND.getText().equals("") || txtDiaChiKH.getText().equals("") || txtSDTKH.getText().equals("") || txtMucDichKH.getText().equals("")) {
            JOptionPane.showConfirmDialog(null, "Các trường nhập không được để trống!", "Thông báo", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try {
        String maKH = txtMaKH.getText();
        ResultSet existingCustomer = ketNoi.executeQuery("SELECT * FROM khachhang WHERE makh = '" + maKH + "';");

        if (!existingCustomer.next()) {
            JOptionPane.showMessageDialog(null, "Mã khách hàng không tồn tại! Mời bạn nhập mã khách hàng khác", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) tbDSKH.getModel();
            int selectedRow = tbDSKH.getSelectedRow();
            
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn một khách hàng từ bảng để sửa thông tin.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            Boolean thanhToanValue = (Boolean) model.getValueAt(selectedRow, 7); // Assuming 6 is the column index for thanhToan
            boolean thanhToan = thanhToanValue != null && thanhToanValue;

            String updateQuery = "UPDATE khachhang SET "
                    + "hoten = '" + txtHoTenKH.getText() + "', "
                    + "ngaysinh = '" + new SimpleDateFormat("yyyy-MM-dd").format(txtNgaySinh.getDate()) + "', "
                    + "diachi = '" + txtDiaChiKH.getText() + "', "
                    + "sdt = '" + txtSDTKH.getText() + "', "
                    + "mucdich = '" + txtMucDichKH.getText() + "', "
                    + "cmnd = '" + txtCMND.getText() + "', "
                    + "thanhToan = " + (thanhToan ? 1 : 0) + " "
                    + "WHERE makh = '" + maKH + "';";

            ketNoi.executeUpdate(updateQuery);

            // Clear the text fields after successful update
            txtMaKH.setText("");
            txtHoTenKH.setText("");
            txtCMND.setText("");
            txtDiaChiKH.setText("");
            txtSDTKH.setText("");
            txtMucDichKH.setText("");

            // Reload the data in the table
            loadData("SELECT makh, hoten, cmnd, ngaysinh, diachi, sdt, mucdich, thanhtoan FROM khachhang;");
            
            int result = ketNoi.executeUpdate(updateQuery);

            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Sửa khách hàng thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Sửa khách hàng thất bại.", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btSuaKHActionPerformed


