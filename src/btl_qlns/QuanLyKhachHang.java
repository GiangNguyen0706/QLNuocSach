    private void tbDSKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDSKHMouseClicked
        int row = tbDSKH.getSelectedRow();
        if (row >= 0) {
            DefaultTableModel model = (DefaultTableModel) tbDSKH.getModel();
            txtMaKH.setText(model.getValueAt(row, 0).toString());
            txtHoTenKH.setText(model.getValueAt(row, 1).toString());
            txtCMND.setText(model.getValueAt(row, 2).toString());
            try {
                txtNgaySinh.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(row, 3).toString()));
            } catch (ParseException ex) {
                Logger.getLogger(QuanLyHopDong.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtDiaChiKH.setText(model.getValueAt(row, 4).toString());
            txtSDTKH.setText(model.getValueAt(row, 5).toString());
            txtMucDichKH.setText(model.getValueAt(row, 6).toString());
        }
    }//GEN-LAST:event_tbDSKHMouseClicked

    private void btTimKiemKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btTimKiemKHMouseClicked
        String searchKeyword = txtTimKiemKH.getText().trim();

        // Check if the search keyword is not empty
        if (!searchKeyword.isEmpty()) {
            // Load data with the search query
            loadData("SELECT makh, hoten, cmnd, ngaysinh, diachi, sdt, mucdich FROM khachhang WHERE hoten LIKE '%" + searchKeyword + "%'");
        } else {
            // If the search keyword is empty, reload all data
            loadData("SELECT makh, hoten, cmnd, ngaysinh, diachi, sdt, mucdich FROM khachhang");
        }
    }//GEN-LAST:event_btTimKiemKHMouseClicked

