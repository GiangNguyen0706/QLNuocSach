    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        // TODO add your handling code here:
        // Iterate through JTable rows
        int successCount = 0;
        DefaultTableModel model = (DefaultTableModel) tbDSKH.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String maKH = model.getValueAt(i, 0).toString();
            String hoTen = model.getValueAt(i, 1).toString();
            String cmnd = model.getValueAt(i, 2).toString();
            String ngaySinh = model.getValueAt(i, 3).toString();
            String diaChi = model.getValueAt(i, 4).toString();
            String sdt = model.getValueAt(i, 5).toString();
            String mucDich = model.getValueAt(i, 6).toString();
            Boolean thanhToanValue = (Boolean) model.getValueAt(i, 7);
            boolean thanhToan = (thanhToanValue != null && thanhToanValue);

            // Update the database based on the retrieved values
            String updateQuery = "UPDATE khachhang SET "
                    + "hoten = '" + hoTen + "', "
                    + "cmnd = '" + cmnd + "', "
                    + "ngaysinh = '" + ngaySinh + "', "
                    + "diachi = '" + diaChi + "', "
                    + "sdt = '" + sdt + "', "
                    + "mucdich = '" + mucDich + "', "
                    + "thanhtoan = " + (thanhToan ? 1 : 0) + " " // Assuming 1 represents true and 0 represents false
                    + "WHERE makh = '" + maKH + "'";

            int result = ketNoi.executeUpdate(updateQuery);
            if (result > 0) {
                successCount++;
            }
        }
        if (successCount == model.getRowCount()) {
            JOptionPane.showMessageDialog(null, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_UpdateActionPerformed

